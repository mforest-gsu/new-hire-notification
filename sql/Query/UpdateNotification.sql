  HRE_NOTIFY_CONTEXT = :HRE_NOTIFY_CONTEXT,
  HRE_NOTIFY_STATUS_CODE = :HRE_NOTIFY_STATUS_CODE,
  HRE_NOTIFY_STATUS_DESC = :HRE_NOTIFY_STATUS_DESC
