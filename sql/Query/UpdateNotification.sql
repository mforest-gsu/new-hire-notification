  HRE_NOTIFY_ID = :HRE_NOTIFY_ID AND
  HRE_NOTIFY_USER_ID = :HRE_NOTIFY_USER_ID AND
  HRE_NOTIFY_TEMPLATE = :HRE_NOTIFY_TEMPLATE AND
  HRE_NOTIFY_TIMESTAMP = TO_DATE(:HRE_NOTIFY_TIMESTAMP, 'yyyy-mm-dd hh24:mi:ss')
